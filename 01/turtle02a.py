import turtle as t

t.speed(100)

def draw_square():
    t.forward(100)
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.forward(100)
    t.right(90)
    t.forward(100)
    t.forward(100)
    t.right(90)

draw_square()
t.forward(200)
draw_square()

t.done()
